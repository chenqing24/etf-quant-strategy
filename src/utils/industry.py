def get_industry(code: str) -> str:
    """获取ETF行业分类"""
    return INDUSTRY_MAPPING.get(code, '其他')


def get_industry_name(code: str) -> str:
    """获取ETF行业中文名"""
    industry = get_industry(code)
    return INDUSTRY_NAMES.get(industry, '其他')


def get_all_industries() -> Dict[str, int]:
    """获取所有行业及ETF数量"""
    from collections import Counter
    return dict(Counter(INDUSTRY_MAPPING.values()))


